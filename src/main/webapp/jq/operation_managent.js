    function addTab1(title,url){
    	console.log(title);
    	var state = $("#ttt").tabs("exists", title);
    	if (state){
    		$('#ttt').tabs('select', title);
    	} else {
    		var content = '<iframe scrolling="auto" frameborder="0"  src="'+url+'" style="width:100%;height:100%;"></iframe>';
    		$('#ttt').tabs('add',{
    			title:title,
    			content:content,
    			closable:true
    		});
    	}
    }
    
